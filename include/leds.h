static inline void put_pixel(PIO pio, uint sm, uint32_t pixel_grb) {
    pio_sm_put_blocking(pio, sm, pixel_grb << 8u);
}

static inline uint32_t urgb_u32(uint8_t r, uint8_t g, uint8_t b) {
    return
            ((uint32_t) (r) << 8) |
            ((uint32_t) (g) << 16) |
            (uint32_t) (b);
}

static inline uint32_t urgbw_u32(uint8_t r, uint8_t g, uint8_t b, uint8_t w) {
    return
            ((uint32_t) (r) << 8) |
            ((uint32_t) (g) << 16) |
            ((uint32_t) (w) << 24) |
            (uint32_t) (b);
}

#define RED urgb_u32(0xf, 0x0, 0x0)
#define GREEN urgb_u32(0x0, 0xf, 0x0)
#define BLUE urgb_u32(0x0, 0x0, 0xf)
#define CYAN urgb_u32(0x0, 0xf, 0xf)
#define MAGENTA urgb_u32(0xf, 0x0, 0xf)
#define YELLOW urgb_u32(0xf, 0xf, 0x0)

inline uint32_t colors[] = {RED, GREEN, BLUE, CYAN, MAGENTA, YELLOW};
